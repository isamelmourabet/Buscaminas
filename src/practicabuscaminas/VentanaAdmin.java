    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        File selectedFile = null;
        int returnValue = jfc.showOpenDialog(null);

        try {
            if (returnValue == JFileChooser.APPROVE_OPTION) {
                selectedFile = jfc.getSelectedFile();
            }
            admin.cargaListado(selectedFile);
            jTextArea1.append("Archivo cargado\n");

        } catch (IOException e) {
            jTextArea1.append("ERROR E/S\n");
        } catch (NullPointerException n) {

        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void crearTxtButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearTxtButton3ActionPerformed
        try (BufferedReader entrada = new BufferedReader(new FileReader(new File("infopartida.txt")))) {
            String linea;

            while ((linea = entrada.readLine()) != null) {
                jTextArea1.append(linea + "\n");
            }
        } catch (FileNotFoundException e) {
            jTextArea1.append("Error: Archivo no encontrado");
            e.printStackTrace();
        } catch (IOException e) {
            System.err.println("Error E/S");
            e.printStackTrace();
        }

    }//GEN-LAST:event_crearTxtButton3ActionPerformed

