    private void registrarseButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarseButton1ActionPerformed
        String nombre = nombreTextField1.getText();
        String contrasenia = contrasenaTextField2.getText();
        Jugador jug = new Jugador(nombre, contrasenia);

        if ("".equals(jug.getNombre()) || "".equals(jug.getContrasena())) {
            jTextArea1.append("Usuario no válido");
            nombreTextField1.setText("");
            contrasenaTextField2.setText("");

        } else if (!admin.autenticar(jug)) {
            admin.alta(jug);
            jTextArea1.append("Jugador: " + nombre + " añadido correctamente\n");

        } else {
            jTextArea1.append("El jugador ya esta en dado de alta\n");

        }
        nombreTextField1.setText("");
        contrasenaTextField2.setText("");
    }//GEN-LAST:event_registrarseButton1ActionPerformed

    private void darBajaButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_darBajaButton2ActionPerformed

        String nombre = nombreTextField1.getText();
        String contrasenia = contrasenaTextField2.getText();
        Jugador jug = new Jugador(nombre, contrasenia);

        if ("".equals(jug.getNombre()) || "".equals(jug.getContrasena())) {
            jTextArea1.append("Usuario no válido");
            nombreTextField1.setText("");
            contrasenaTextField2.setText("");

        } else if (admin.autenticar(jug)) {
            admin.baja(jug);
            jTextArea1.append("Jugador: " + nombre + " ha sido dado de baja\n");

        } else {
            jTextArea1.append("El jugador aún no esta registrado\n");

        }
        nombreTextField1.setText("");
        contrasenaTextField2.setText("");

    }//GEN-LAST:event_darBajaButton2ActionPerformed

